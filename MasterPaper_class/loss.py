def dice(pred, true):
    pred[pred>=0.5] = 1
    pred[pred<0.5] = 0
    inter = np.sum(pred * true)
    uni = np.sum(pred) + np.sum(true)
    if uni == 0:
        if inter == 0:
            return 1
        else:
            return 0
    return inter * 2 / uni

def dsc_loss(input, target):
    N = target.size(0)
    smooth = 1
    input_flat = input.view(N, -1)
    target_flat = target.view(N, -1)
    intersection = input_flat * target_flat

    loss = (intersection.sum(1) * 2 + smooth) / (input_flat.sum(1) + target_flat.sum(1) + smooth)
    return 1-loss

def focal_loss1(y_pred, y_true):
    y_pred = torch.clamp(y_pred, 0.00001, 0.99999)
    pt_1 = torch.where(torch.eq(y_true, 1), y_pred, torch.ones_like(y_pred))
    pt_0 = torch.where(torch.eq(y_true, 0), y_pred, torch.zeros_like(y_pred))
    return -torch.mean(0.25*torch.pow(1.0 - pt_1, 2) * torch.log(pt_1)) - torch.mean(0.75*torch.pow(pt_0, 2)* torch.log(1.0 - pt_0))

class DiceLoss(nn.Module):
    def __init__(self, weight=None, size_average=True):
        super(DiceLoss, self).__init__()

    def forward(self, inputs, targets, smooth=1):
        
        #comment out if your model contains a sigmoid or equivalent activation layer
        inputs = F.sigmoid(inputs)     
        
        #flatten label and prediction tensors
        inputs = inputs.view(-1)
        targets = targets.view(-1)
        
        intersection = (inputs * targets).sum()                            
        dice = (2.*intersection + smooth)/(inputs.sum() + targets.sum() + smooth)  
        
        return 1 - dice

